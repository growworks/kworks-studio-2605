type Variant = 'header' | 'footer' | 'floating'

/**
 * SNS 아이콘 묶음.
 * 데이터는 SITE.social 배열에서 가져오며, type 별 SVG 패스를 inline 으로 그린다.
 * 새 SNS 추가는 site.ts 의 social 배열에 한 줄 추가 + 아래 ICONS 에 SVG 추가만 하면 된다.
 */
export function SocialIcons({ variant }: { variant: Variant }) {
  if (SITE.social.length === 0) return null

  const className =
    variant === 'header'
      ? 'kw-header__social'
      : variant === 'footer'
        ? 'kw-footer__social'
        : 'kw-floating-social'

  return (
    <div className={className}>
      {SITE.social.map((s) => (
        <a
          key={s.type}
          href={s.url}
          aria-label={s.label}
          target="_blank"
          rel="noopener noreferrer"
        >
          {ICONS[s.type]}
        </a>
      ))}
    </div>
  )
}

